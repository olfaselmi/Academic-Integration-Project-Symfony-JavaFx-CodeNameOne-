public class modenseig {
    private int id;
    private int idEnseig;
    private int idMod;

    public modenseig(int id, int idEnseig, int idMod) {
        this.id = id;
        this.idEnseig = idEnseig;
        this.idMod = idMod;
    }

    public int getId() {
        return id;
    }

    public int getIdEnseig() {
        return idEnseig;
    }

    public int getIdMod() {
        return idMod;
    }
    
    
            
    
}
